export const sixMonths = () => {
	return new Date(Date.now() + 183 * 24 * 60 * 60 * 1000);
};

export const oneMonth = () => {
	return new Date(Date.now() + 31 * 24 * 60 * 60 * 1000);
};

export const fifteenMinutes = () => {
	return new Date(Date.now() + 1000 * 60 * 15);
};

export const ONE_DAY_MS = 24 * 60 * 60 * 1000;
